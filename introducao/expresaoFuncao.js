/*
Expressao de funcao
uma funcao sem nome, que geralmente se utiliza uma const 
pois assim, nao corre o risco de ser reatribuida no programa
e como constante precisa receber um valor para funcionar
*/

const soma = function(n1, n2){return n1 + n2}
console.log(soma(100,20))

const divide = function(n1,n2){return n1 / n2}
console.log(divide(20,4))